public class Operaciones {
    public double v1(double c,double e,double f,double b){
        return (c*e)-(f*b);
    }

    public double v2(double a,double e,double d,double b){
        return (a*e)-(d*b);
    }

    public double v3(double a,double f,double d,double c){
        return (a*f)-(d*c);
    }

    public double v4(double a,double e,double d,double b){
        return (a*e)-(d*b);
    }
}
